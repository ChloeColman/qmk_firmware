layer_state_t layer_state_set_user(layer_state_t state) {
    // Auto enable scroll mode on MOUSE layer only (Keyball layer uses cursor)
    keyball_set_scroll_mode(get_highest_layer(state) == U_MOUSE);
    return state;
}

// Bongo cat split sync - sync tap events from master to slave
typedef struct {
    uint8_t tap_count;  // Increments on each tap
} bongo_sync_t;

static bongo_sync_t bongo_local = {0};   // Local state
static bongo_sync_t bongo_remote = {0};  // Received from other half

// RPC handler - called on slave when master sends tap
void bongo_sync_handler(uint8_t in_len, const void* in_data, uint8_t out_len, void* out_data) {
    const bongo_sync_t* received = (const bongo_sync_t*)in_data;
    bongo_remote = *received;
}

void keyboard_post_init_user(void) {
    // Register split sync handler
    transaction_register_rpc(USER_SYNC_BONGO, bongo_sync_handler);
}

// Called on keypress - send tap to other half
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static uint16_t scrl_btn_timer;

    // Handle custom SCRL_BTN: tap = middle click, hold = scroll mode
    if (keycode == SCRL_BTN) {
        if (record->event.pressed) {
            scrl_btn_timer = timer_read();
            keyball_set_scroll_mode(true);
        } else {
            keyball_set_scroll_mode(get_highest_layer(layer_state) == U_MOUSE);
            if (timer_elapsed(scrl_btn_timer) < TAPPING_TERM) {
                tap_code(MS_BTN3);  // Middle click on tap
            }
        }
        return false;
    }

    if (record->event.pressed) {
        bongo_local.tap_count++;
        // Send to other half
        transaction_rpc_send(USER_SYNC_BONGO, sizeof(bongo_local), &bongo_local);
    }
    return true;
}

