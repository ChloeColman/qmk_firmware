// Bongo cat animation - responds to keypresses from BOTH halves!
void oledkit_render_logo_user(void) {
    // Track tap count to detect new input from either half
    static uint8_t prev_local_count = 0;
    static uint8_t prev_remote_count = 0;
    static uint16_t tap_timer = 0;
    static bool paw_down = false;
    static bool which_paw = false;  // alternates each tap

    // Check for new taps from local or remote (other half)
    if (bongo_local.tap_count != prev_local_count || bongo_remote.tap_count != prev_remote_count) {
        paw_down = true;
        which_paw = !which_paw;  // Alternate paws
        tap_timer = timer_read();
        prev_local_count = bongo_local.tap_count;
        prev_remote_count = bongo_remote.tap_count;
    }

    // Paw stays down for 150ms then releases
    if (paw_down && timer_elapsed(tap_timer) > 150) {
        paw_down = false;
    }

    // Clear and position for drawing
    oled_clear();
    oled_set_cursor(0, 0);

    // Draw the appropriate frame
    if (paw_down && which_paw) {
        oled_write_raw_P(bongo_left, BONGO_SIZE);
    } else if (paw_down && !which_paw) {
        oled_write_raw_P(bongo_right, BONGO_SIZE);
    } else {
        oled_write_raw_P(bongo_idle, BONGO_SIZE);
    }
}

